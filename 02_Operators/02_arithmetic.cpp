#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 3;
    
    int addition = a + b;
    cout << addition << endl;
    
    int subtraction = a - b;
    cout << subtraction << endl;
    
    int multiplication = a * b;
    cout << multiplication << endl;
    
    int division = a / b;
    cout << division << endl;
    
    int modulus = a % b;
    cout << modulus << endl;
    
    return 0;
}