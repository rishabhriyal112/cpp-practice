#include <iostream>
using namespace std;

int main(){
    int a =5; //Integer
    cout << a << endl;

    float b =2.5; //Decimal Values
    cout << b << endl;

    char c = 'hello' ; //Character Stored
    cout << c << endl;

    string d = "Hello"; //String Stored
    cout << d << endl;

    bool e = true; //Boolean Value
    cout << e << endl;
    
    return 0;

}
